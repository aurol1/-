function jiaoyan_img = jiaoyan(img,a,b)
[w,h,r]=size(img);
jiaoyan_img = img;
k1=a;
k2=b;
a1=rand(w,h)<k1;
a2=rand(w,h)<k2;
t1=jiaoyan_img(:,:,1);
t2=jiaoyan_img(:,:,2);
t3=jiaoyan_img(:,:,3);
t1(a1&a2)=0;
t2(a1&a2)=0;
t3(a1&a2)=0;
t1(a1& ~a2)=255;
t2(a1& ~a2)=255;
t3(a1& ~a2)=255;
jiaoyan_img(:,:,1)=t1;
jiaoyan_img(:,:,2)=t2;
jiaoyan_img(:,:,3)=t3;
